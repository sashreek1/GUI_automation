def open_git():
    l= Label(root, text='user id / email :', font=26)
    l.place(x=165, y=345)
    textbox = Entry(root)
    textbox.place(x=165,y=365)
    l1= Label(root, text='password:', font=26)
    l1.place(x=165, y=395)
    textbox1 = Entry(root,show="*")
    textbox1.place(x=165, y=415)
    s = Button(root, text="Submit",command = lambda: s_command(textbox,textbox1,l,l1,s))
    s.place(x=210, y=445)
