#!/usr/bin/env python3
from tkinter import *
from selenium import webdriver
import time


def s_command(textbox,textbox1,l,l1,s):
    usr = textbox.get()
    psd = textbox1.get()
    driver = webdriver.Firefox()
    driver.get("https://github.com/login")
    time.sleep(1)
    username = driver.find_element_by_xpath('//*[@id="login_field"]')
    username.send_keys(usr)
    password = driver.find_element_by_xpath('//*[@id="password"]')
    password.send_keys(psd)
    sub = driver.find_element_by_xpath('/html/body/div[3]/main/div/form/div[3]/input[8]')
    sub.click()
    l.place_forget()
    l1.place_forget()
    textbox1.place_forget()
    textbox.place_forget()
    s.place_forget()
